//WAP to print 1 to 10 odd numbers using For loop
for(let i=1;i<10;i=i+2){
    console.log(i)
}

//WAP to print 1 to 10 odd numbers using while loop
let num=1
while(num<10){
    console.log(num)
    num=num+2
}

//WAP to print 1 to 10 odd numbers using do while loop
let p=1
do{
    console.log(p)
    p=p+2
}while(p<10)