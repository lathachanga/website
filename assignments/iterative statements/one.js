//WAP to print 1 to 10 numbers using a while loop
let num=1
while(num<=10){
    console.log(num)
    num++
}

//WAP to print 1 to 10 numbers using a for loop
for(let i=1;i<=10;i++){
    console.log(i)
}

//WAP  to print 1 to 10 numbers using a do while loop
let p=1
do{
    console.log(p)
    p++
}while(p<=10);
   
