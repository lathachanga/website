//WAP to print a multiplication table of 6 using a while loop
let i=1;
while(i<=10){
     console.log("6*",i,"=", 6*i)
     i=i+1;
}

//WAP to print a multiplication table of 6 using a do while loop
let num=1
do{
     console.log("6*",num,"=",6*num)
     num++
}while(num<=10)

//WAP to print a multiplication table of 6 using a for loop
for(let p=1;p<=10;p++){
     console.log("6*",p,"=",6*p)
}