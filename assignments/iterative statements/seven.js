//WAP  first ten multiples of 4 using a while loop
let i=4;
while(i<=40){
    console.log(i)
    i=i+4
}

//WAP  first ten multiples of 4 using a for loop
for(let p=4;p<=40;p=p+4){
    console.log(p)
}

//WAP  first ten multiples of 4 using a do while loop
let num=4
do{
    console.log(num)
    num=num+4
}while(num<=40)