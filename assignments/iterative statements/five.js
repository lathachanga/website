//WAP to print 1 to 10 even numbers using a while loop
let i=2
while(i<10){
    console.log(i)
    i=i+2
}

//WAP to print 1 to 10 even numbers using a for loop
for(let p=2;p<10;p=p+2){
    console.log(p)
}

//WAP to print 1 to 10 even numbers using a do while loop
let num=2
do{
    console.log(num)
    num=num+2
}while(num<10)