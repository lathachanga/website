//WAP factors of 24 using while loop
let i=1
while(i<=24){
    if(24%i==0)
    console.log(i)
    i++
}

//WAP factors of 24 using do while loop
let p=1
do{
    if(24%p==0)
    console.log(p)
    p++
}while(p<=24)

//WAP factors of 24 using forloop
for(let num=1;num<=24;num++){
    if(24%num==0){
        console.log(num)
    }
}