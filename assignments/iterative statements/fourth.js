//WAP to print 15 to 10 numbers using for loop
for(let i=15;i>=10;i--){
    console.log(i)
}

//WAP to print 15 to 10 numbers using while loop
let num=15
while(num>=10){
    console.log(num)
    num--
}

//WAP to print 15 to 10 numbers using do while loop
let p=15
do{
    console.log(p)
    p--
}while(p>=10)
