//WAP to print 10 to 1 number using a while loop
let num=10
while(num>=1){
    console.log(num)
    num--
}

//WAP to print 10 to 1 number using a for loop
for(let i=10;i>=1;i--){
    console.log(i)
}

//WAP to print 10 to 1 number using a do while loop
let p=10
do{
    console.log(p)
    p--
}while(p>=1)